package com.example.lasalliansimulator;

public class Player {
    private int health;
    private int social;
    private int money;
    private int grades;

    public int getHealth() {
        return health;
    }

    public int getSocial() {
        return social;
    }

    public void setSocial(int social) {
        this.social = social;
    }

    public int getMoney() {
        return money;
    }

    public void setMoney(int money) {
        this.money = money;
    }

    public int getGrades() {
        return grades;
    }

    public void setGrades(int grades) {
        this.grades = grades;
    }

    public void setHealth(int health) {
        this.health = health;
    }



}
